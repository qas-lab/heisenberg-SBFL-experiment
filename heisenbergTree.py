        num_strings = 0

        #For each string specified in the test case, perform Pauli propagation through the circuit and store the results
        for pauli_string, pauli_coeff in zip(test.keys(), test.values()):

            num_strings += 1
            # Define the initial Pauli operator to track
            # Qiskit layout is right-to-left: 'IX' means X acts on Qubit 0, I acts on Qubit 1
            initial_pauli = SparsePauliOp(pauli_string)  
            num_qubits = circuit_inverse.num_qubits

            # This dictionary will store our automated history mapping
            # Format: { Step_Index: { "gate_type": gate_name, "pauli_strings": { "Pauli_String": Coefficient } } }
            evolution_summary_map = {}
            transition_graph = {}  # step -> list of (from, to, coeff)

            # Log the initial state (Step 0) before any gates apply
            evolution_summary_map[0] = {
                "gate_type": "Initial",
                "pauli_strings": {
                    pauli.to_label(): np.round(coeff, 4) 
                    for pauli, coeff in zip(initial_pauli.paulis, initial_pauli.coeffs)
                }
            }

            #Define the current layer of the evolution tree, starting with the initial Pauli having a probability of 100% to appear
            current_layer = {
                initial_pauli.paulis[0].to_label(): 1.0  # Start with the initial Pauli having probability 1
            }

            #Create the do-nothing operator that can be manipulated to act on specific bits
            identity_op = Operator.from_label("I" * num_qubits)

            for step_idx, (instruction, qargs, cargs) in enumerate(circuit_inverse.data, start=1):

                gate_type = instruction.name
                q_indices = [qarg._index for qarg in qargs]

                # Detect whether transfer rules exist
                has_rule = gate_type in transferRules

                # Fallback operator path only if needed
                # if not has_rule and not is_instruction_clifford(instruction):
                gate_op = identity_op.compose(Operator(instruction), q_indices)

                new_layer = {}
                edges = []

                for pauli_in, prob_in in current_layer.items():

                    pauli_label = pauli_in.to_label() if hasattr(pauli_in, "to_label") else str(pauli_in)

                    #TODO: Implement transfer rules to speed up evolution calculation
