import numpy as np
from tqdm import tqdm
import pandas as pd
import json
from qiskit.quantum_info import Pauli, SparsePauliOp, Operator
from qiskit import QuantumCircuit
from heisenbergUtilities import *

"""
The goal of this method is to perform heisenberg evolution on the provided circuit.

INPUTS:
    circuit_inverse: The inverse of the QASM circuit to be analyzed, found by using the load_program method on a QASM file then doing circuit.copy().inverse()

    operation_list: A linked list of each gate in the inverse circuit

    testcase_batch: The list of test cases generated by SB-QOPS. 
                    It should contain a collection of passing or failing test cases, 
                    inside each test case being a collection of Pauli strings that construct it along with their weights

    pass_fail: A string indicating if the testcase_batch is a batch of failing or passing test cases

                    
OUTPUTS:
    testcases_analysis: A pandas DataFrame whose columns should be the gates of the circuit + a column specifying pass or fail. 
                        The rows should be the probability counts of how often gates were involved in a circuit per test case
"""
def heisenberg_evolve(circuit_inverse, operation_list, testcase_batch, pass_fail, lambda_phase, lambda_change, atol = 1e-4, max_terms = None, search_step = None):
    
    testcase_analysis = create_global_test_dataframe(operation_list)
    num_qubits = circuit_inverse.num_qubits
    identity_op = Operator.from_label("I" * num_qubits)
    first = True

    for test in tqdm(testcase_batch):

        test = format_test_case(test, first)
        num_strings = 0

        #For each string specified in the test case, perform Pauli propagation through the circuit and store the results
        for pauli_string, pauli_coeff in zip(test.keys(), test.values()):

            num_strings += 1
            # Define the initial Pauli operator to track
            # Qiskit layout is right-to-left: 'IX' means X acts on Qubit 0, I acts on Qubit 1
            initial_pauli = SparsePauliOp(pauli_string)  

            transition_graph = {}  # step -> list of (from, to, coeff)

            #Define the current layer of the evolution tree, starting with the initial Pauli having a probability of 100% to appear
            current_layer = {
                initial_pauli.paulis[0].to_label(): 1.0  # Start with the initial Pauli having probability 1
            }

            for step_idx, (instruction, qargs, cargs) in enumerate(circuit_inverse.data, start=1):

                #Define operation being performed
                gate_type = instruction.name 
                gate_op = Operator(instruction)

                #Create a do-nothing operator, then add the desired gate to the do-nothing to only act on the desired qubits
                q_indices = [qarg._index for qarg in qargs]
                full_gate_op = identity_op.compose(gate_op, q_indices)

                new_layer = {}
                edges = []

                for pauli_in, prob_in in current_layer.items():

                    evolved_dict = evolve_pauli_exact(SparsePauliOp(pauli_in), SparsePauliOp.from_operator(full_gate_op), tolerance=atol, terms=max_terms, search_step=search_step)
                    # accumulate results
                    for pauli_out, coeff_out in evolved_dict.items():

                        total_prob = prob_in * (abs(coeff_out) ** 2)

                        new_layer[pauli_out] = new_layer.get(pauli_out, 0) + total_prob

                        edges.append({
                            "from": pauli_in,
                            "to": pauli_out,
                            "weight": coeff_out,
                            "probability": total_prob,
                            "phase": np.angle(coeff_out),
                            "sign": coeff_out / abs(coeff_out) if coeff_out != 0 else 0,
                        })

                # prune noise
                current_layer = {
                    Pauli(p): prob
                    for p, prob in new_layer.items()
                    if abs(prob) > 1e-4
                }

                transition_graph[step_idx] = {
                    "gate_type": gate_type,
                    "edges": edges
                }

            operation_list = add_counts_to_linked_list(operation_list, transition_graph, pauli_coeff, lambda_phase, lambda_change)

        first = False
        testcase_analysis = append_to_analysis(testcase_analysis, operation_list, num_strings, pass_fail)
        operation_list.reset()

    return testcase_analysis
